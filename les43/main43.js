// switch case

var date = 9

switch(date)　{
    case 2: {　// date === 2
        console.log('今日は月曜日です。')
        break
    }
    case 3: {
        console.log('今日は火曜日です。')
        break
    }
    case 4: {
        console.log('今日は水曜日です。')
        break
    }
    case 5: {
        console.log('今日は木曜日です。')
        break
    }
    case 6: {
        console.log('今日は金曜日です。')
        break
    }
    case 7: {
        console.log('今日は土曜日です。')
        break
    }
    case 8: {
        console.log('今日は日曜日です。')
        break
    }
    default: console.log('今日は何曜日ですか。')
}